package fr.peralta.mycellar.interfaces.facades.stock.dto;

import java.io.Serializable;

import fr.peralta.mycellar.interfaces.facades.wine.dto.Country;

/**
 * @author speralta
 * 
 */
public class Bottle implements Serializable {

    private static final long serialVersionUID = 685255958663623266L;

    private int id;

    private Country country;

    private float price;

    private int quantity;

    /**
     * @return the country
     */
    public Country getCountry() {
        return country;
    }

    /**
     * @param country
     *            the country to set
     */
    public void setCountry(Country country) {
        this.country = country;
    }

    /**
     * @return the quantity
     */
    public int getQuantity() {
        return quantity;
    }

    /**
     * @param quantity
     *            the quantity to set
     */
    public void setQuantity(int number) {
        this.quantity = number;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id
     *            the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the price
     */
    public float getPrice() {
        return price;
    }

    /**
     * @param price
     *            the price to set
     */
    public void setPrice(float price) {
        this.price = price;
    }

    public String getLabel() {
        StringBuilder builder = new StringBuilder();
        builder.append(country.getName());
        return builder.toString();
    }

}
